package entity;

public class Damage {
	
	private String user_id;
	private String sbook_id;
	private String description;
	private String damage_fine;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getSbook_id() {
		return sbook_id;
	}
	public void setSbook_id(String sbook_id) {
		this.sbook_id = sbook_id;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDamage_fine() {
		return damage_fine;
	}
	public void setDamage_fine(String damage_fine) {
		this.damage_fine = damage_fine;
	}
	
	

}
